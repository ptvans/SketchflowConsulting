export function getCareerById(id: string): Career | undefined {
  return CAREERS.find(c => c.id === id);
}

export function getCareersByCategory(category: string): Career[] {
  return CAREERS.filter(c => c.categories.includes(category));
}

export function getCareersByIncomeMin(minIncome: number): Career[] {
  return CAREERS.filter(c => c.incomeRange.max >= minIncome);
}
